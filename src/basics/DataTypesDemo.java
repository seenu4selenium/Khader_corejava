package basics;

public class DataTypesDemo {

	public static void main(String[] args) {
		//User will store the numbers into a variable.
		byte b = 27;
		short c = 345;
		int a = 968979;
		long d = 1874632210;
		System.out.println(a);
		
		
		//User will store number with decimal values into a variable.
		float f = 56.123456789f;
		double g = 56.123456789;
		
		//Want to store TRUE  or FALSE, will use boolean
		boolean h = true;
		boolean i = false;
		
		//User will store number/Alphabet/SpecialCharacter into a variable.
		//char data type you can declare into single quotes
		char abc ='4';
//		char abc ='p';
//		char abc ='&';
		

	}

}
